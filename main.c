void PWM(int time){
    int ctrl=0;
    while(ctrl<time){
        //PWM al 25%
        PR2=0xFF;

        CCPR1L=0x40;
        CCP1CONbits.DC1B0=0x00;
        CCP1CONbits.DC1B1=0x00;

        TRISCbits.RC2=0x00;

        T2CONbits.T2CKPS=0x03;

        CCP1CONbits.CCP1M0=0x01;
        CCP1CONbits.CCP1M1=0x01;
        CCP1CONbits.CCP1M2=0x01;
        CCP1CONbits.CCP1M3=0x01;

        TMR2=0x00;

        T2CONbits.TMR2ON=0x01;
        DELAY_sec(1);
        ctrl++;
    }
    CCPR1L=0xFF;
    CCP1CONbits.DC1B0=0x01;
    CCP1CONbits.DC1B1=0x01;
}

void Config(){
    T1CON = 0x01;               //Configure Timer1 interrupt
    PIE1bits.TMR1IE = 1;           
    INTCONbits.PEIE = 1;
    RCONbits.IPEN=0x01;
    IPR1bits.TMR1IP=0x01;            // TMR1 high priority ,TMR1 Overflow Interrupt Priority bit
    INTCONbits.GIE = 1;
    PIR1bits.TMR1IF = 0;
    T0CON=0X00;
    INTCONbits.T0IE = 1;               // Enable interrupt on TMR0 overflow
    INTCON2bits.TMR0IP=0x00;        
    T0CONbits.TMR0ON = 1;
    
    
    GLCD_Init();
    ADCON1bits.PCFG=0x0C;
    ADCON1bits.VCFG=0x00;
    ADCON0bits.CHS=0x00;
    ADCON0bits.GODONE=0x00;
    ADCON0bits.ADON=0x00;
    ADCON2bits.ACQT=0x02;
    ADCON2bits.ADCS=0x05;
    ADCON2bits.ADFM=0x01;
    
    TRISC=0xFF;
    
}
