void __interrupt() getKeyVal(void)             // High priority interrupt
{
    if (TMR1IE && TMR1IF)
    {
        if(PORTCbits.RC1==1){
            onOff=~onOff;
        }
        KeyBoard=analogRead(0);
        if(PORTCbits.RC1==1){
            std=!std;
            while(1){
                if(PORTCbits.RC1==0){
                    break;
                }
            }
        }
        TMR1IF=0;
    }
}

